def parse_ethernet_frame(frame: bytes) -> Dict:
    """
    Parse an 802.1Q Ethernet frame.
    
    Args:
        frame: Raw frame bytes (64 bytes minimum)
        
    Returns:
        Dictionary with parsed fields
    """
    if len(frame) < 64:
        raise ValueError(f"Frame too short: {len(frame)} bytes")
    
    # Parse header
    dst_mac = frame[0:6]
    src_mac = frame[6:12]
    tpid = struct.unpack('!H', frame[12:14])[0]
    
    # Check for VLAN tag
    if tpid == TPID_8021Q:
        tci = struct.unpack('!H', frame[14:16])[0]
        vlan_id = tci & 0x0FFF
        pcp = (tci >> 13) & 0x07
        dei = (tci >> 12) & 0x01
        ethertype = struct.unpack('!H', frame[16:18])[0]
        payload = frame[18:64]
    else:
        # No VLAN tag (shouldn't happen in our system)
        vlan_id = None
        pcp = None
        dei = None
        ethertype = tpid
        payload = frame[14:64]
    
    return {
        'dst_mac': dst_mac.hex(':'),
        'src_mac': src_mac.hex(':'),
        'vlan_id': vlan_id,
        'pcp': pcp,
        'dei': dei,
        'ethertype': ethertype,
        'payload_size': len(payload),
        'total_size': len(frame)
    }


def parse_counter_values(
    counter_array: np.ndarray,
    num_neurons: int = 2880,
    ports_per_switch: int = 96
) -> np.ndarray:
    """
    Parse hardware counter values into neuron activation array.
    
    With temporal multiplexing, multiple neurons map to each port.
    This function reconstructs the full neuron activation vector.
    
    Args:
        counter_array: Raw counter values from switch ports
        num_neurons: Total neurons in network (2880)
        ports_per_switch: Ports per switch (96)
        
    Returns:
        Neuron activation vector (2880 values)
    """
    # For now, assume simple 1:1 mapping for testing
    # TODO: Implement temporal multiplexing demux when hardware arrives
    
    if len(counter_array) < num_neurons:
        # Pad with zeros if needed
        activations = np.zeros(num_neurons, dtype=np.uint64)
        activations[:len(counter_array)] = counter_array
    else:
        activations = counter_array[:num_neurons]
    
    return activations


