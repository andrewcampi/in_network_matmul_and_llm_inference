import struct
import time
import numpy as np
from typing import List, Tuple, Dict
from dataclasses import dataclass


# =============================================================================
# PACKET STRUCTURE CONSTANTS
# =============================================================================

# Ethernet frame sizes
ETH_HEADER_SIZE = 14  # bytes (6 dst + 6 src + 2 type)
VLAN_TAG_SIZE = 4     # bytes (2 TPID + 2 TCI)
MIN_PAYLOAD_SIZE = 46 # bytes (to reach 64-byte minimum)
FCS_SIZE = 4          # bytes (CRC, auto-generated by NIC)
MIN_FRAME_SIZE = 64   # bytes total (including VLAN tag)

# Ethernet constants
BROADCAST_MAC = b'\xff\xff\xff\xff\xff\xff'
TPID_8021Q = 0x8100   # 802.1Q VLAN tag identifier
ETHERTYPE_IPV4 = 0x0800  # Not used, but standard placeholder


# =============================================================================
# DATA STRUCTURES
# =============================================================================

@dataclass
class NeuronPacket:
    """Represents a single packet encoding neuron activation."""
    neuron_id: int
    layer_id: int
    activation_value: int  # Encoded as packet count
    timestamp: float = 0.0
    
    def __repr__(self):
        return f"NeuronPacket(n={self.neuron_id}, L={self.layer_id}, act={self.activation_value})"


@dataclass
class PacketBatch:
    """Batch of packets for one token inference."""
    packets: List[bytes]
    neuron_count: int
    layer_id: int
    total_bytes: int
    generation_time_us: float


# =============================================================================
# PACKET CRAFTING
# =============================================================================

def craft_ethernet_frame(
    dst_mac: bytes,
    src_mac: bytes,
    vlan_id: int,
    payload: bytes = None
) -> bytes:
    """
    Craft a complete 802.1Q VLAN-tagged Ethernet frame.
    
    Frame structure (64 bytes minimum):
    ┌──────────────────────────────────────┐
    │ Destination MAC (6 bytes)            │
    │ Source MAC (6 bytes)                 │
    │ TPID: 0x8100 (2 bytes)               │
    │ TCI: PCP|DEI|VLAN_ID (2 bytes)       │
    │ EtherType (2 bytes)                  │
    │ Payload (46 bytes, padded)           │
    │ [FCS: 4 bytes, added by NIC]         │
    └──────────────────────────────────────┘
    
    Args:
        dst_mac: Destination MAC address (6 bytes)
        src_mac: Source MAC address (6 bytes)
        vlan_id: VLAN ID (0-4095), encodes layer ID
        payload: Optional payload bytes
        
    Returns:
        Complete Ethernet frame (64 bytes, excluding FCS)
    """
    # Ethernet header (14 bytes)
    eth_header = dst_mac + src_mac
    
    # VLAN tag (4 bytes)
    # TCI format: 3 bits PCP | 1 bit DEI | 12 bits VLAN ID
    tci = vlan_id & 0x0FFF  # Ensure 12-bit VLAN ID
    vlan_tag = struct.pack('!HH', TPID_8021Q, tci)
    
    # EtherType (2 bytes)
    ethertype = struct.pack('!H', ETHERTYPE_IPV4)
    
    # Payload (46 bytes, padded with zeros)
    if payload is None:
        payload = b'\x00' * MIN_PAYLOAD_SIZE
    else:
        # Pad to minimum size
        if len(payload) < MIN_PAYLOAD_SIZE:
            payload = payload + b'\x00' * (MIN_PAYLOAD_SIZE - len(payload))
    
    # Assemble frame (FCS will be added by NIC hardware)
    frame = eth_header + vlan_tag + ethertype + payload
    
    # Verify size
    assert len(frame) == 64, f"Frame size {len(frame)} != 64 bytes"
    
    return frame


def generate_neuron_packets(
    neuron_id: int,
    activation_value: int,
    layer_id: int,
    src_mac: bytes,
    dst_mac: bytes = BROADCAST_MAC
) -> List[bytes]:
    """
    Generate packets for a single neuron.
    
    Activation encoding: activation_value=N → generate N identical packets
    
    Args:
        neuron_id: Neuron identifier (0-2879)
        activation_value: Neuron activation (0-255)
        layer_id: Current layer (0-31)
        src_mac: Source MAC address (host NIC)
        dst_mac: Destination MAC (usually broadcast)
        
    Returns:
        List of raw Ethernet frames (bytes)
    """
    packets = []
    
    for _ in range(activation_value):
        frame = craft_ethernet_frame(
            dst_mac=dst_mac,
            src_mac=src_mac,
            vlan_id=layer_id
        )
        packets.append(frame)
    
    return packets


def generate_token_packets(
    activation_vector: np.ndarray,
    layer_id: int = 0,
    src_mac: bytes = b'\x00\x1a\x2b\x3c\x4d\x5e'
) -> PacketBatch:
    """
    Generate all packets for one token inference.
    
    Args:
        activation_vector: Neuron activations (2880 values, 0-255)
        layer_id: Initial layer ID (typically 0)
        src_mac: Source MAC address
        
    Returns:
        PacketBatch with all packets and metadata
    """
    start_time = time.perf_counter()
    
    all_packets = []
    total_packets = 0
    
    for neuron_id, activation in enumerate(activation_vector):
        activation_int = int(np.clip(activation, 0, 255))
        
        if activation_int > 0:
            packets = generate_neuron_packets(
                neuron_id=neuron_id,
                activation_value=activation_int,
                layer_id=layer_id,
                src_mac=src_mac
            )
            all_packets.extend(packets)
            total_packets += len(packets)
    
    end_time = time.perf_counter()
    generation_time_us = (end_time - start_time) * 1e6
    
    batch = PacketBatch(
        packets=all_packets,
        neuron_count=len(activation_vector),
        layer_id=layer_id,
        total_bytes=total_packets * 64,
        generation_time_us=generation_time_us
    )
    
    return batch


