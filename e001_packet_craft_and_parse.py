def validate_packet_structure(frame: bytes) -> Tuple[bool, str]:
    """
    Validate that a packet has correct structure.
    
    Returns:
        (is_valid, error_message)
    """
    if len(frame) != 64:
        return False, f"Wrong size: {len(frame)} bytes (expected 64)"
    
    # Check VLAN tag
    tpid = struct.unpack('!H', frame[12:14])[0]
    if tpid != TPID_8021Q:
        return False, f"Missing VLAN tag: TPID={hex(tpid)}"
    
    # Check VLAN ID is in valid range
    tci = struct.unpack('!H', frame[14:16])[0]
    vlan_id = tci & 0x0FFF
    if vlan_id > 4095:
        return False, f"Invalid VLAN ID: {vlan_id}"
    
    return True, "OK"


def test_packet_generation_performance(
    num_neurons: int = 2880,
    avg_activation: float = 5.0,
    num_trials: int = 100
) -> Dict:
    """
    Benchmark packet generation performance.
    
    Target: <20 μs for 2,880 packets
    
    Returns:
        Performance statistics
    """
    print(f"\n{'='*60}")
    print(f"PACKET GENERATION PERFORMANCE TEST")
    print(f"{'='*60}")
    print(f"Neurons: {num_neurons}")
    print(f"Avg activation: {avg_activation}")
    print(f"Trials: {num_trials}")
    
    times = []
    packet_counts = []
    
    for trial in range(num_trials):
        # Generate random activation vector
        activations = np.random.poisson(avg_activation, num_neurons)
        activations = np.clip(activations, 0, 255).astype(np.uint8)
        
        # Generate packets
        batch = generate_token_packets(activations)
        
        times.append(batch.generation_time_us)
        packet_counts.append(len(batch.packets))
    
    stats = {
        'mean_time_us': np.mean(times),
        'std_time_us': np.std(times),
        'min_time_us': np.min(times),
        'max_time_us': np.max(times),
        'mean_packet_count': np.mean(packet_counts),
        'total_bytes_avg': np.mean(packet_counts) * 64
    }
    
    print(f"\nResults:")
    print(f"  Mean time:        {stats['mean_time_us']:.2f} μs")
    print(f"  Std dev:          {stats['std_time_us']:.2f} μs")
    print(f"  Min time:         {stats['min_time_us']:.2f} μs")
    print(f"  Max time:         {stats['max_time_us']:.2f} μs")
    print(f"  Avg packets:      {stats['mean_packet_count']:.0f}")
    print(f"  Avg total bytes:  {stats['total_bytes_avg']:.0f}")
    
    # Check against target
    target_us = 20.0
    if stats['mean_time_us'] <= target_us:
        print(f"\n✓ PASS: {stats['mean_time_us']:.2f} μs <= {target_us} μs target")
    else:
        print(f"\n✗ WARNING: {stats['mean_time_us']:.2f} μs > {target_us} μs target")
        print(f"  Slowdown factor: {stats['mean_time_us']/target_us:.2f}×")
    
    return stats


def test_packet_round_trip():
    """
    Test packet generation and parsing round-trip.
    """
    print(f"\n{'='*60}")
    print(f"PACKET ROUND-TRIP TEST")
    print(f"{'='*60}")
    
    # Generate test packet
    src_mac = b'\xaa\xbb\xcc\xdd\xee\xff'
    layer_id = 5
    
    packets = generate_neuron_packets(
        neuron_id=42,
        activation_value=3,
        layer_id=layer_id,
        src_mac=src_mac
    )
    
    print(f"\nGenerated {len(packets)} packets for neuron 42, layer {layer_id}")
    
    # Parse first packet
    parsed = parse_ethernet_frame(packets[0])
    
    print(f"\nParsed packet:")
    print(f"  Source MAC:   {parsed['src_mac']}")
    print(f"  Dest MAC:     {parsed['dst_mac']}")
    print(f"  VLAN ID:      {parsed['vlan_id']}")
    print(f"  EtherType:    {hex(parsed['ethertype'])}")
    print(f"  Payload size: {parsed['payload_size']} bytes")
    print(f"  Total size:   {parsed['total_size']} bytes")
    
    # Validate
    is_valid, msg = validate_packet_structure(packets[0])
    print(f"\nValidation: {msg}")
    
    if parsed['vlan_id'] == layer_id:
        print("✓ VLAN ID matches")
    else:
        print(f"✗ VLAN ID mismatch: expected {layer_id}, got {parsed['vlan_id']}")
    
    if parsed['src_mac'] == src_mac.hex(':'):
        print("✓ Source MAC matches")
    else:
        print(f"✗ Source MAC mismatch")
    
    return is_valid


